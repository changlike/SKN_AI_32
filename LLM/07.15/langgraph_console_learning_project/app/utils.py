"""콘솔 출력에 공통으로 사용하는 보조 함수입니다."""


def print_title(title: str) -> None:
    """실습 제목을 보기 좋게 출력합니다."""
    # 각 실습 시작 위치를 구분하기 위해 빈 줄을 출력합니다.
    print()
    # 일정한 길이의 구분선을 출력합니다.
    print("=" * 80)
    # 전달받은 제목을 대괄호 형태로 출력합니다.
    print(f"[{title}]")
    # 제목 아래에도 같은 구분선을 출력합니다.
    print("=" * 80)


def pause() -> None:
    """사용자가 결과를 읽을 수 있도록 Enter 입력을 기다립니다."""
    # Enter 키 입력 전까지 메인 메뉴로 돌아가지 않도록 대기합니다.
    input("\nEnter 키를 누르면 메인 메뉴로 돌아갑니다.")
